//1友元是一个类将其私有内容授权到外界的一种许可，是一个类或函数直接处理另一个类内容
//的有效方式和通行证。使一个函数或一个类处理另一个类的方式有三层：一，直接在被处理
//中定义“招待“函数将某个函数或类或类成员函数定义为形参，进行处理。主动权在被处理类，
//也相当于被处理类提供被处理接口。
//或在处理类中定义处理函数以被处理类为形参只能以被处理类提供的接口进行处理。
//二、将处理类的某个函数声明为被处理类的友元函数，使得不通过招待接口直接进行处理。
//三、将处理类全部或说其本身直接声明为被处理类的友元类，使得处理类的所有函数
//不通过接口函数获得被处理类的所有使用权。这三层是一个从监督接口开放到特定类
//到授权到特定类，再到全部授权到特定类的过程。
//2友元是两个特定类之间的授权开放问题，而一个类中的public和private则是一个类向
//其它所有类的开放问题。另加protected则是继承类的开放问题。
//3友元属于面向对象封装的范畴。类型转换等属于多态。
//封装和多态是面向对象编程很重要的两个概念。
